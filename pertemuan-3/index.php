<?php
/* 
PENGULANGAN
for
while
do .. while  
foreach : pengulangan khusus array
*/

// for
for ($i = 0; $i < 5; $i++) {
    echo "Hello World !!! <br>";
}

// while
echo "<br>";
$a = 1;
while ($a <= 5) {
    echo "Hai World !!! <br>";
    $a++;
}

// do ... while
echo "<br>";
$b = 1;
do {
    echo "Hallo World !!! <br>";
    $b++;
} while ($b <= 5);